def which(cmd):
    for p in os.environ["PATH"].split(os.pathsep):
        f = os.path.join(p, cmd)
        if os.path.exists(f):
            return (f, cmd)
    return None

installer = which("apt-get")
if installer is None:
    installer = which("dnf")
if installer is None:
    installer = which("yum")
if installer is None:
    installer = which("zypper")

def sucall(cmd):
    if installer is None:
        print("Skipping (no package manager):", cmd)
        return
    if os.getuid() == 0:
        call(cmd)
    elif which("sudo") is not None:
        call(["sudo"] + cmd)
    else:
        print("Skipping (no sudo):", cmd)

def pkg_install(pkg):
    if installer is None:
        print("Skipping package (no package manager):", pkg)
        return
    sucall([installer[1], "install", "-y", pkg])

if which("vim") is None:
    pkg_install("vim")
if which("find") is None:
    pkg_install("findutils")
if which("which") is None:
    pkg_install("which")
if which("file") is None:
    pkg_install("file")
if which("perl") is None:
    pkg_install("perl")
if which("hostname") is None:
    pkg_install("hostname")

